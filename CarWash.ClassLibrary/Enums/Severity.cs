        /// Indicates an error message.
        /// </summary>
        Error,

        /// <summary>
        /// Indicates a warning message.
        /// </summary>
        Warning,

        /// <summary>
        /// Indicates an informational message.
        /// </summary>
        Info,

        /// <summary>
        /// Indicates a success message.
        /// </summary>
        Success
    }
}
